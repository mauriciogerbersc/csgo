     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        DB::beginTransaction();
        $jogo = Jogos::create([
            'jogo' => $request->jogo
        ]);
        DB::commit();

        $request->session()->flash('mensagem', "Jogo <strong>{$jogo->jogo}</strong> cadastrado com sucesso.");
        $request->session()->flash('alert_tipo', "alert-success");

        return redirect()->route('listar_jogos');
    }

